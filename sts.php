                    directly or through another non-centralized broker/exchange.
                </p>
                <h3 class="display-6 mb-2">Stock Broker Fraud</h3>
                <p class="mb-2">
                    Stock Broker Fraud Stock broker fraud is, thankfully, one of the
                    forms of fraud that has decreased as stock markets have become
                    more regulated. However, that doesn’t mean it doesn’t happen. The
                    days of a stockbroker taking the old physical paper securities or
                    deposited cash and running away are long gone. Stockbrokers who
                    wish to do wrong by the clients today now have to resort to more
                    shady methods. One of the most prevalent (and, sometimes legal)
                    forms of stockbroker fraud is an activity known as
                    ‘front-running.’ Let’s say you were to call your brokerage firm
                    and you wanted to buy 100 shares of XYZ. Your broker, knowing that
                    this would likely move the market, instead places an order himself
                    before executing your order. The broker essentially enters the
                    market early, for himself, before completing your order and allows
                    your order to drive up the value of his earlier entry.
                </p>
                <p class="mb-2">
                    The major problem with front-running is it is nearly impossible to
                    detect. With the amount of high-frequency-trading (HFT) that
                    occurs every second, front-running is a type of fraud that only
                    diligent and active regulators can determine. There are other
                    tactics that stockbrokers will employ that are not necessarily
                    fraudulent but unethical. Some types of behavior are encouraging
                    high-frequency day trading by new investors, undisclosed
                    appropriation of dividends, and unclear rules or fees associated
                    with shorting.
                </p>
