                <h1 class="display-6 mb-5">Types of Securities fraud</h1>
                <h3 class="display-6 mb-2">Ponzi Schemes</h3>
                <p class="mb-2">
                    Perhaps the most well-known version of a stock fraud scheme in
                    history is a Ponzi scheme. A Ponzi scheme involves paying
                    investment profits to old investors with the deposits of new
                    investors. When a new investor joins the system, it is most often
                    at the advice of another investor unknowingly part of the Ponzi
                    scheme. What makes Ponzi schemes so successful is the perceived
                    legitimacy and history of returns. Ponzi schemes can exist for
                    years or even decades.
                </p>
                <p class="mb-2">
                    In the case of Bernie Madoff, his Ponzi scheme and fraud empire
                    lasted for over 20 years. He was so successful at this fraud that
                    his firm even became one of the leading market makers of the stock
                    market, and he was a former chairman of the NASDAQ! Ponzi schemes
                    eventually fail because there is never enough new investor money
                    to keep paying off old investors. Some of the keywords or phrases
                    with a Ponzi scheme are ‘guaranteed income,’ ‘offshore
                    investment,’ ‘small, private hedge fund,’ ‘secret invite-only
                    fund,’ or any other form of ‘too good to be true’ sales pitch.
                </p>
                <h3 class="display-6 mb-2">Pump and Dump</h3>
                <p class="mb-2">
                    Pump and Dump (sometimes referred to as P&Ds) scams are perhaps
                    one of the classic and persistent forms of stock fraud that exist.
                    What makes challenging to detect is that they can often be done in
                    a legitimate fashion. A pump and dump is essentially an event
                    where you are targeted to buy a stock. The stock is sometimes
                    referred to as a ‘multi-bagger,’ indicating a massive return on
                    investment. You may also read about how the stock is an emerging
                    leader in its sector/industry, how it could be the next Apple or
                    Amazon.
                </p>
                <p class="mb-2">
                    The stocks used in pump and dump are often publicly traded and
                    listed on a regulated exchange like the NASDAQ and bought through
                    a regulated stockbroker. The stock price is almost always between
                    $1.00 to $10.00; how a pump and dump works is straightforward. The
                    scammers purchase the cheap stock early then begins to campaign on
                    its behalf and drum up support and enthusiasm. As new investors
                    buy, the price starts to spike up and accelerate – it is at that
                    point the originators of the scam sell and exit their positions at
                    a significant profit and at the expense of yourself and everyone
                    else who bought too late.
                </p>
